
public class Node {
   
    private int Number;
    private Node Right;
    private Node Left;
   
    public int getNumber() {
        return Number;
    }
    public void setNumber(int number) {
        Number = number;
    }
    public Node getRight() {
        return Right;
    }
    public void setRight(Node right) {
        Right = right;
    }
    public Node getLeft() {
        return Left;
    }
    public void setLeft(Node left) {
        Left = left;
    }
   
}